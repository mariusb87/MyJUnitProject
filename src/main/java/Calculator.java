public class Calculator {
    public int add(int x, int y){
        return x+y;
    }
    public int dif(int x, int y){

        return x-y;
    }
    public int multiply(int x, int y){

        return x*y;
    }
    public double divide(int x, int y){
        return x/y;
    }

    public int modulo(int x, int y){
        return x%y;
    }
}
